    private void jButtonHitungLuasKelilingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitungLuasKelilingActionPerformed
        try {
            double diagonal1 = Double.parseDouble(jDiagonal1Field.getText());
            double diagonal2 = Double.parseDouble(jDiagonal2Field.getText());

            // Buat objek dan hitung luas dan keliling
            BelahKetupat belahKetupat = new BelahKetupat(diagonal1, diagonal2);
            double luas = belahKetupat.hitungLuas();
            belahKetupat.hitungSisi();
            double keliling = belahKetupat.hitungKeliling();

            // Tampilkan hasil
            jLabelHasilLuas.setText("Luas Belah Ketupat      : " + String.format("%.2f", luas) + " cmÂ²"); // Format ke 2 desimal
            jLabelHasilKeliling.setText("Keliling Belah Ketupat : " + String.format("%.2f", keliling) + " cm"); // Format ke 2 desimal
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "Masukkan angka yang valid untuk kedua diagonal", // Pesan error lebih spesifik
                "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                e.getMessage(), // Pesan error dari validasi negatif atau nol
                "Perhitungan Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonHitungLuasKelilingActionPerformed

