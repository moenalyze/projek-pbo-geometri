    private void jButtonHitungLuasKelilingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHitungLuasKelilingActionPerformed
        try {
            double jariJari = Double.parseDouble(jJariJariField.getText());
            double sudut = Double.parseDouble(jSudutField.getText());

            // Buat objek dan hitung luas dan keliling
            JuringLingkaran juringLingkaran = new JuringLingkaran(jariJari, sudut);
            double luas = juringLingkaran.hitungLuas();
            double keliling = juringLingkaran.hitungKeliling();

            // Tampilkan hasil
            jLabelHasilLuas.setText("Luas Juring Lingkaran      : " + String.format("%.2f", luas) + " cmÂ²"); // Format ke 2 desimal
            jLabelHasilKeliling.setText("Keliling Juring Lingkaran : " + String.format("%.2f", keliling) + " cm"); // Format ke 2 desimal
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                "Masukkan angka yang valid untuk jari-jari dan sudut", // Pesan error lebih spesifik
                "Input Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(this,
                e.getMessage(), // Pesan error dari validasi negatif atau nol
                "Perhitungan Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonHitungLuasKelilingActionPerformed

